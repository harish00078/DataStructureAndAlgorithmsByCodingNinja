package MileStone1Lecture11;



public class QuestionOfInputAndPrint2dArray {

    public static void main(String[] args) {

        int [][] arr = new int[2][2];
        for(int i = 0; i < 2; i++){
            for(int j = 0; j < 2; j++){
                System.out.println(arr[i][j] + " ");
            }
        }
        
    }
    
}
